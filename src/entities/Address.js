export class Address {
  anrede
  briefanrede
  titel
  geschlecht
  vorname
  nachname

  constructor (anrede, briefanrede, titel, geschlecht, vorname, nachname) {
    this.anrede = anrede
    this.briefanrede = briefanrede
    this.titel = titel
    this.geschlecht = geschlecht
    this.vorname = vorname
    this.nachname = nachname
  }
}
